#!/usr/bin/env bash
# voltdots ‚ÄĒ install.sh
# Creates symlinks from ~/.voltdots to ~/.config
# Safe to run multiple times ‚ÄĒ existing symlinks are updated

DOTFILES="$HOME/.voltdots"
CONFIG="$HOME/.config"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
